float	ft_cos(float x)
{
	float	cos;

	x += 1.57079632;
	if (x > 3.14159265)
		x -= 6.28318531;
	if (x < 0)
		cos = 1.27323954 * x + 0.405284735 * x * x;
	else
		cos = 1.27323954 * x - 0.405284735 * x * x;
	return (cos);
}
