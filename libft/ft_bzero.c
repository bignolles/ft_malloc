#include <stdlib.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned int	i;

	i = 0;
	if (n != 0)
	{
		while (i < n)
		{
			*((char *)s + i) = '\00';
			i++;
		}
	}
}
