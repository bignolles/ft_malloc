float	ft_sin(float x)
{
	float	sin;

	if (x < -3.14159265)
		x += 6.28318531;
	else if (x > 3.14159265)
		x -= 6.28318531;
	if (x < 0)
		sin = 1.27323954 * x + .405284735 * x * x;
	else
		sin = 1.27323954 * x - 0.405284735 * x * x;
	return (sin);
}
