#include <libft.h>

float	ft_arccos(float nbr)
{
	float	res;

	res = (-0.54038583f * ft_pow(nbr, 5)) + (-0.14191872f * ft_pow(nbr, 4)) +
			(0.07485317f * ft_pow(nbr, 3)) + (0.06303811f * ft_pow(nbr, 2)) +
			(-1.02113558f * nbr) + 1.56817611f;
	return (res);
}
