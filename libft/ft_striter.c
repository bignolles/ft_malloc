#include <stdlib.h>

void	ft_striter(char *s, void (*f)(char *))
{
	unsigned int	i;

	i = 0;
	if (s != NULL && f != NULL)
	{
		while (s[i] != '\00')
		{
			f(s + i);
			i++;
		}
	}
}
