float	ft_sqrt(float nbr)
{
	long		i;
	float		x2;
	float		y;

	x2 = nbr * 0.5F;
	y = nbr;
	i = *(long *)&y;
	i = 0x5F375A86 - (i >> 1);
	y = *(float *)&i;
	y = y * (1.5F - (x2 * y * y));
	y = 1 / y;
	if (y < 0.0)
		y = -y;
	return (y);
}
